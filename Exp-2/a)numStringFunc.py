# Program to demonstrate integers and strings
name= "Ameer" # Assigning a string to the variable name
age= 17 # Assigning an integer to the variable age
print("Name:", name) # Printing the name
print("Age:", age) # Printing the age
print(name + " is " + str(age) + " years old") # Concatenating strings and integers
# The + operator is used to concatenate strings
# The str() function converts the integer age to a string for concatenation
# The above code demonstrates how to work with strings and integers in Python